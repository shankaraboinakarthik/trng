SELECT
EMP.ENAME,DEPT.LOC
FROM EMP JOIN DEPT
ON EMP.DEPTNO=DEPT.DEPTNO;



SELECT
EMP.ENAME,DEPT.LOC
FROM EMP JOIN DEPT
ON EMP.DEPTNO=DEPT.DEPTNO
ORDER BY DEPT.LOC;






SELECT E.EMPNO, E.ENAME
FROM EMP E JOIN EMP M
ON E.MGR=M.EMPNO AND E.HIREDATE<M.HIREDATE;




SELECT EMP.* , dEPT.LOC
